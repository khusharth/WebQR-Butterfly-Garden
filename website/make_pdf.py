def pdf(name, adult, children, total, time):
    from reportlab.pdfgen import canvas
    canvas = canvas.Canvas("static/website/pdf/print.pdf", pagesize=(250,470))
    canvas.setLineWidth(.3)
    canvas.setFont('Helvetica', 18)
    canvas.drawInlineImage("static/website/images/butterfly.jpeg", 70,420, width=42,height=42)
    canvas.drawString(132,445,'Butterfly')
    canvas.drawString(132,425,'Garden')
    canvas.line(10,416,240,416)
    canvas.drawInlineImage("qrcodes/qrcode.png", 53, 271, width=140,height=140)
    canvas.line(10,264,240,264)
    canvas.setFont('Helvetica', 16)
    canvas.drawString(20,240,'Total : '+str(total))
    canvas.setFont('Helvetica', 12)
    canvas.drawString(20,220,'Name : '+str(name))
    canvas.drawString(20,203,'Adult : '+str(adult))
    canvas.drawString(120,203,'Children : '+str(children))
    canvas.drawString(20,185,'Time : '+str(time))
    canvas.line(10,169,240,169)
    canvas.line(10,170,240,170)
    canvas.drawInlineImage("qrcodes/qrcode.png", 53, 20, width=140,height=140)
    canvas.save()
